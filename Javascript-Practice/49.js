//iterables
//where we can use for of loop
//strings and arrays are iterables


//strings example
const firstName = "Komal"
for(let char of firstName) {
    console.log(char)
}


//array example
const items = ["Komal", "Prachita", "Samyukta"]
for(let item of items) {
    console.log(item)
}

